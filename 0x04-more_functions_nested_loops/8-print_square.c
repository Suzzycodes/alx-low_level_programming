#include "main.h"
/**
 * print_square - prints sqaures
 * @size: is the size of the square
 */

void print_square(int size)
{
	int i;
	int j;

	if (size <= 0)
		_putchar('\n');
	else
	{
		for (j = 0 ; j < i ; j++)
		{
			for (i = 0 ; i < size ; i++)
			{
				_putchar('#');
			}
		_putchar('\n');
		}
	}
}
